    private double power(double a1, double a2) {
        double output = 0;
        if (a2 > 0) {
            output = a1;
            for (int i = 1; i < a2; i++) {
                output = output * a1;
            }
        } else if (a2 < 0) {
            output = a1;
            for (int i = -1; i > a2; i--) {
                output = output * a1;
            }
            output = 1 / output;
        } else if (a2 == 0) {
            output = 1;
        }
        return output;
    }

    private double cylinder(double a1, double a2) {
        double output;
        output = Math.round((a1 * a1 * a2 * 3.14159265) * 10000.0) / 10000.0;
        return output;
    }

    private double sphere(double a1) {
        double output;
        output = Math.round(((4 * a1 * a1 * a1 * 3.14159265) / 3) * 10000.0) / 10000.0;
        return output;
    }

